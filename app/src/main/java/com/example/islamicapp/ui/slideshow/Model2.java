package com.example.islamicapp.ui.slideshow;

public class Model2 {

    String English;
    String Arabic;
    String Meaning;

    public Model2(String english, String arabic, String meaning) {
        English = english;
        Arabic = arabic;
        Meaning = meaning;
    }

    public String getEnglish() {
        return English;
    }

    public void setEnglish(String english) {
        English = english;
    }

    public String getArabic() {
        return Arabic;
    }

    public void setArabic(String arabic) {
        Arabic = arabic;
    }

    public String getMeaning() {
        return Meaning;
    }

    public void setMeaning(String meaning) {
        Meaning = meaning;
    }
}
